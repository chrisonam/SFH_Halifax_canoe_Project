<h2>Step 1: Request a password reset email</h2>
<p>If you’ve forgotten your password, request a password reset email. The password reset email expires after one hour.</p>
<p>If you have any trouble receiving your password reset link, please contact our Customer Care team.</p>

<h2>Step 2: Use the email to reset your password</h2>
<ol>

<li><strong>Find the password reset email that we sent you.</strong></li>
<ul>
<li>If you can’t find the email, check your spam filter.</li>
<li>If the email says there is no When I Work account associated with the email address you entered, follow the steps in I Can’t Reset My Password.</li>
</ul>
<li><strong>In the email, click Reset your password.</strong><li>
    <ul>
<li>If you’re using a computer or an iOS device, enter a new password in the webpage that opens in your browser.
<li>If you’re using an Android device, open the link using the When I Work app if you’re prompted. Enter a new password, then tap Checkmark button in the top-right corner of the screen.</li>
</ul>